                        <h5 class="tipo-letra">Marzo 2023 - Pichilemu, Chile.</h5>
                        <p>
                            Luego de años trabajando en una empresa de Santiago como informático, estresado la mayoria del tiempo, donde el ritmo de vida es acelerado y todo es caotico, La vida me premio y tuve la oportunidad de ir a vivir a Pichilemu a disfrutar de mis vacaciones indefinidas acompañado de mi compañera de ese entonces junto a su perrita llamada Flo.
                            <br><br>
                            Durante el transcurso de los dias, dimos con una publicación para la adopción y hogar temporal de perros callejeros que se encontraban en la misma comuna y no dudé en vivir la experiencia de ayudar siendo por primera vez hogar temporal de uno de estos peludos de la calle, Asi fue como conocimos al pequeño Tommy!, un macho de 6 meses de edad.
                            <br><br>
                            Los primeros días fueron un desafío; Tommy estaba delgado, desconfiado, temerozo y cojo pero con amor, paciencia y atención veterinaria, comenzó a florecer. Su cola, que antes estaba baja, ahora se movía con alegría. Era evidente que su historia estaba tomando un giro positivo.
                            <br><br>
                            Con el tiempo, la experiencia de acoger a Tommy me llevó a reflexionar sobre la cantidad de perros y gatos callejeros que necesitaban ayuda. La idea de hacer algo más grande tomó forma, y así nació la inspiración para crear una página web de adopción de mascotas callejeras.
                            <br><br>
                            Tommy se convirtió en el símbolo de esta iniciativa, recordándonos constantemente que un pequeño acto de compasión podía cambiar vidas, tanto humanas como la de ellos mismos. La página web no solo se convirtió en un espacio virtual, sino en un puente que unía a las familias con sus futuras mascotas y, al mismo tiempo, en un recordatorio de la increíble capacidad de los animales para transformar nuestras vidas.
                            <br><br>
                            Con la llegada de Tommy en marzo de 2023, empezamos una nueva etapa de amor, compromiso, esperanza y sanación, tuvo la suerte ser adoptado por una familia pichilemina el 24 de Junio del mismo año gracias a la ayuda de su Rescatista y todas las personas que colaboraron de alguna forma para que Tommy sea un perrito feliz. 
